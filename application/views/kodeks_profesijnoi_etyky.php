						<h3>
							Обязанности брокера по отношению к клиенту
						</h3>
						<p>
							Берясь за выполнение определенных в договоре действий, брокер обязан их осуществить – как с точки зрения действующего законодательства, так и экономических последствий несоблюдения условий договора.
						</p>
						<p>
							Специфической обязанностью брокера является то, что интересы клиента должны быть поставлены выше интересов других субъектов, с тем условием, однако, что это не подрывает доброе имя данного брокера или брокерской профессии, или же непосредственные интересы брокера как хозяйственного субъекта. Для этого он должен, выполняя обязательства, предусмотренные договором, как можно лучше применить свою постоянно совершенствуемую профессиональную квалификацию. Он также должен действовать в соответствии с самыми высокими принципами профессиональной добросовестности. Выполнение вышеупомянутых обязанностей имеет место, в частности, тогда, когда брокер:
						</p>
						<ul>
							<li>выбирает учреждение страхования с хорошим финансовым положением и вместе с тем пользующееся его доверием,</li>
							<li>помогает клиенту в управлении риском, в том числе – разрабатывает программу страхования, оптимально соответствующую потребностям клиента,</li>
							<li>информирует клиента о принципах и правилах, действующих в страховании, а также объясняет связанные с ними права и обязанности сторон,</li>
							<li>принимает на работу персонал, имеющий соответствующую, высокую квалификацию,</li>
							<li>соблюдает прерогативы клиента, в частности, имеющие информационный характер,</li>
							<li>обеспечивает конфиденциальность полученной от клиента информации.</li>
						</ul>
						<h3>
							Обязанности брокера в отношении учреждения страхования
						</h3>
						<p>
							Учитывая определенное расхождение интересов страхователя и учреждения страхования, брокер должен действовать как своего рода третейский судья: соблюдать приоритет интересов клиента и, вместе с этим, учитывать истинные интересы страхового учреждения. В связи с этим брокер:
						</p>
						<ul>
							<li>должен:
								<ul>
									<li>представлять предложения, как можно лучше подтвержденные документами и в полном объеме,</li>
									<li>давать ответы на все вопросы учреждения страхования, которые касаются размещенных рисков,</li>
									<li>предоставлять различные возможные указания, которые могут пригодиться для разрешения сомнений учреждения страхования,</li>
									<li>стремиться к усовершенствованию сотрудничества с учреждением страхования,</li>
									<li>объяснить клиенту, в чем заключается принцип добрых намерений (наивысшего доверия) в отношениях с учреждением страхования;</li>
								</ul>
							</li>
							<li>не должен:
								<ul>
									<li>способствовать созданию в учреждении страхования ошибочного представления о риске,</li>
									<li>поддерживать необоснованные претензии клиента, в частности, он не должен путем мошенничества способствовать осуществлению ненадлежащих страховых выплат,</li>
									<li>в поисках самых выгодных для клиента условий способствовать систематическому снижению цены на услуги, которое может угрожать платежеспособности учреждения страхования,</li>
									<li>распространять неточные, недостаточно подтвержденные документами или просто фальшивые данные об учреждении страхования.</li>
								</ul>
							</li>
						</ul>
						<h3>
							 Обязанности брокера относительно коллег-конкурентов
						</h3>
						<p>
						Брокер, осуществляя свои услуги, влияет на рынок страхового посредничества и тем самым на положение субъектов, участвующих в нем. В связи с этим, кроме возможных правовых последствий (например, связанных с применением правовых положений, противодействующих недобросовестной конкуренции), могут возникнуть постулаты морального характера. Они требуют, чтобы брокер, выполняя свою роль, осуществлял ее, используя предполагающие лояльность принципы добросовестной конкуренции. Для этого брокер:
						</p>
						<ul>
							<li>должен:
								<ul>
									<li>относиться к своим коллегам с уважением, вежливо, беспристрастно и с доверием,</li>
									<li>действуя совместно с другими брокерами, скрупулёзно и пунктуально соблюдать обязательства,</li>
									<li>хранить в тайне конфиденциальную информацию, полученную от коллег в процессе выполнения своих профессиональных обязанностей,</li>
									<li>избегать связей с учреждениями страхования, которые не признают брокерских обычаев,</li>
									<li>разрешать возможные споры с коллегами, прежде всего, в ходе конструктивного диалога,</li>
									<li>делиться своими знаниями и опытом с коллегами – если это непосредственно не угрожает его личным интересам и интересам его клиентов;</li>
								</ul>
							</li>
							<li>не должен:
								<ul>
									<li>клеветать на своих коллег. Возможная оценка компетентности и поведения должна быть основана на соответствующих предпосылках и всегда быть взвешенной,</li>
									<li>хвастаться перед клиентами. Самооценку собственной компетентности должен представлять взвешенно и честно,</li>
									<li>влиять на решение клиента посредством предложения ему – прямо или косвенно – части комиссионных,</li>
									<li>одобрять уменьшение обычно принятых размеров комиссионных за специальную тарификацию рисков,</li>
									<li>подавать вводящие в заблуждение заявления относительно своего профессионального статуса, а также характера и пределов выполняемой роли.</li>
								</ul>
							</li>
						</ul>
						<p>
						Учитывая то, что отношение к другим брокерам в определенной степени детерминировано также позицией по отношению к профессии, брокер должен:
						</p>
						<ul>
							<li>способствовать развитию брокерских услуг,</li>
							<li>самооценку собственной компетентности представлять взвешенно и честно,</li>
							<li>использовать свои знания и опыт в работе, пропагандирующей престиж профессии брокера,</li>
							<li>сотрудничать с Федерацией для защиты профессии брокера, профессионального усовершенствования и поддержки законодательной деятельности, содействующей работе брокеров,</li>
							<li>предупреждать выполнение брокерских операций субъектами, не имеющими на это права,</li>
							<li>реагировать на неэтичное профессиональное поведение своих коллег,</li>
							<li>быть открытым для критики, использовать её для своего профессионального роста,</li>
							<li>воздерживаться от каких-либо действий, которые могут причинить вред доброму имени профессии и страховой деятельности,</li>
							<li>содействовать развитию брокерского самоуправления,</li>
							<li>содействовать распространению высоких стандартов компетентности и профессиональной этики.</li>
