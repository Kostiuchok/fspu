						<h3>Этика в деятельности агента</h3>
						<p>
						В виду отсутствия на украинском рынке страхования разработанных этических норм, касающихся деятельности агента, учреждения страхования – каждое в отдельности – разрабатывают своды моральных норм (обычно именуемые кодексами этики), которые дополнительно определяют принципы поведения агентов, связанных с ними. В них отражается как организационная культура миссии данного учреждения, так и понятия его руководства об особенностях моральных норм относительно этой деятельности.
						</p>
						<p>
						 Учитывая вышеизложенное, и то, что определенные этические нормы, касающиеся страхового посредника (на примере брокера) были представлены выше несколько шире, здесь будут учтены только те нормы, касающиеся деятельности агента, которые, особо подчеркивают специфику работы такого вида посредника.
						</p>
						<h3>Обязанности агента в отношении клиента</h3>
						<ul>
							<li>Принимая во внимание функции страхования – в отношении клиента агент должен – в соответствии с выполняемой им ролью и своими возможностями – чувствовать совместную ответственность за решение, принятое клиентом в сфере страхования. В частности, он должен способствовать удовлетворению его потребностей в страховании, в рамках характеристик угрожающих ему рисков и его финансовых возможностей. Это означает, что он обязан помочь клиенту в осуществлении возможных и необходимых изменений в существующем договоре страхования, а также информировать его об изменениях в продуктах страхования и в новых продуктах.</li>
							<li>Агент должен совершенствовать свою профессиональную квалификацию даже тогда, когда это не связано с его профессиональной карьерой, а служит, прежде всего, повышению качества предоставляемых им услуг.</li>
							<li>Агент должен хранить профессиональную тайну.</li>
							<li>Агент не должен принуждать клиента к отказу или к изменению существующего договора страхования с целью заключения нового.</li>
							<li>Агент должен поддерживать клиента в получении возмещения/ страховой выплаты.</li>
							<li>Агент не должен делать вводящие в заблуждение заявления относительно выполняемой им роли.</li>
						</ul>
						<h3>Обязанности агента в отношении учреждения страхования</h3>
						<p>
						Основной обязанностью агента является лояльность в отношении учреждения страхования, которая проявляется, в частности, в том, что он:
						</p>
						<ul>
							<li>предоставляет информацию о клиенте/риске в соответствии с требованиями заключения договора страхования, в соответствии с законодательством, а также техничными и страховыми принципами работы учреждения страхования,</li>
							<li>не подвергает риску доброе имя учреждения страхования, в том числе не распространяет ложную или непроверенную информацию о нем,</li>
							<li>реагирует на недобросовестную конкуренцию,</li>
							<li>не поддерживает необоснованные претензии клиента, в частности, не входит с ним в сговор с целью получения им ненадлежащего возмещения/ страховой выплаты,</li>
							<li>реагирует на неэтичное поведение других агентов - представителей данного учреждения страхования,</li>
							<li>прилагает усилия, в пределах выполняемой роли, для успешной работы учреждения страхования.</li>
						</ul>
						<h3>Обязанности агента в отношении коллег – агентов</h3>
						<ul>
							<li>Относиться к коллегам с уважением и без злого умысла,</li>
							<li>Не подрывать доброе имя и добросовестность других агентов,</li>
							<li>Разрешать возможные споры конструктивным диалогом,</li>
							<li>Не отказываться в пользу клиента от (части) комиссионных в качестве поощрения для заключения договора страхования,</li>
							<li>Заботиться об интересах профессии агента.</li>
						</ul>
						<p>
						Одной из основных обязанностей членов Федерации страховых посредников Украины является деятельность в рамках действующего законодательства, что честное и этическое поведение каждого посредника важно и необходимо для сохранения доброго имени и чувства единства сообщества.
						</p>
						<p>
						Главные нормы этического поведения посредников, члены Федерации осознанно и добровольно принимают:
						</p>
						<ul>
							<li>  В контактах с Клиентом посредник действует:
								<ul>
									<li>с добрыми намерениями, которые характеризуются уважением и чувством наилучшего использования своих знаний для достижения главной цели – удовлетворения интересов клиента.</li>
								</ul>
							</li>
							<li>Во взаимных контактах:
								<ul>
									<li>каждый из членов Федерации, будучи равным среди равных, пользуется уважением всего сообщества,</li>
									<li>подрыв доброго имени или оскорбление чести другого посредника трактуется в качестве действия, направленного против всего сообщества,</li>
									<li>любая практика и действия, имеющие признаки нечестного обогащения за счет другого посредника, заслуживают на осуждение со стороны всего сообщества.</li>
								</ul>
							</li>
							<li>В контактах со страховым учреждением:
								<ul>
									<li>посредник, посредством своей вовлеченности и профессионализма, умело применяя свои знания, честно ведет себя по отношению к страховому учреждению – стремясь обеспечить интересы клиента, понятого наилучшим образом.</li>
								</ul>
							</li>
						</ul>
						<p>
						Члены Федерации страховых посредников Украины – постановили:
						</p>
						<ul>
							<li>Посредник, член Федерации, поступающий честно и этично всегда может рассчитывать на поддержку и защиту со стороны всего сообщества,</li>
							<li>Посредник, действующий с нарушением общепринятых правил этичного и честного поведения, не только по отношению к другим брокерам, Клиентам либо страховым учреждениям, но и по отношению к третьим лицам, должен быть признан, для блага всего сообщества, независимо от принятых по отношению к нему других мер и процедур – таким, который недостоин членства в Федерации страховых посредников Украины.</li>
						</ul>
						<p>
						 Кодекс определяет стандарты профессиональной этики членов Федерации страховых посредников Украины.
						</p>
						<p>
						Кодекс содержит основные принципы этики и повсеместно признанные нормы профессионального поведения посредников, которые соблюдаются в интересах всех сторон страхового рынка.
						</p>
						<p>
						Рассмотрение претензий по отношению к посредникам, вытекающих из их ошибок и недобросовестного выполнения профессии, относится к соответствующим институциям. Правление Федерации рассматривает заявления и жалобы, касающиеся непрофессионального и неэтического поведения членов Федерации.
						</p>
						<p>
						Страховые/перестраховые брокеры и страховые агенты принимают главные принципы Кодекса Профессиональной Этики и обязуются соблюдать их в деятельности, которой они занимаются.
						</p>
						<p>
						Несоблюдение нижеприведенных принципов может быть оценено Правлением Федерации как неэтическое и привести к санкциям, определенным в Уставе Федерации.
